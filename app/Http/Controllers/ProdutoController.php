    {
        $produto = Produto::create($request->all());
        if($produto->exists()) {
            session()->flash('sucessos','Produto cadastrado com sucesso!');
        }else{
            session()->flash('error','Error ao gravar no banco de dados!');
        }

        return redirect()->route('produto.index');
    }

